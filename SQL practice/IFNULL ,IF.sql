PT_NO	VARCHAR(10)	UNIQUE NOT NULL,
PT_NAME	VARCHAR(20)	NOT NULL,
GEND_CD VARCHAR(1)	NOT NULL,
AGE INT NOT NULL,
TLNO VARCHAR(50) 
);

